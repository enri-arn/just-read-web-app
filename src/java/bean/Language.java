package bean;

/**
 * @author Arnaudo Enrico, Giraudo Paolo, Impeduglia Alessia.
 */
public class Language {

    private int id;
    private String name;

    public Language() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

}
